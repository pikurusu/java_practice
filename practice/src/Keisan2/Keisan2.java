package Keisan2;

/*******************/
/* 四則演算         */
/*******************/

public class Keisan2 {
	public static void main(String[] args) {
		int numberX = 5;
		int numberY = 8;
		int numberZ = 6;

		int answer1 = numberX + numberY;
		System.out.println("答えは：" + answer1);

	    int answer2 = numberY - numberX;
	    System.out.println("答えは：" + answer2);

	    int answer3 = numberX * numberZ;
	    System.out.println("答えは：" + answer3);
	}
}
